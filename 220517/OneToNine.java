// 1부터 9까지 출력하세요~~~~~
public class OneToNine {
    public static void main(String[] args) {
        System.out.print(1);
        System.out.print(2);
        System.out.print(3);
        System.out.print(4);
        System.out.print(5);
        System.out.print(6);
        System.out.print(7);
        System.out.print(8);
        System.out.print(9);
        System.out.print();
        System.out.println("1 2 3\n4 5 6\n7 8 9"); // \n는 1개의 문자로 취급된다. 다음 줄로 넘어가는(줄바꿈),대행 문자
    }
}