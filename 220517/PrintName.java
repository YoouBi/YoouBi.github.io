// 자기 이름 출력하는 프로그램
public class PrintName {
    public static void main(String[] args) {
        System.out.println("장영빈1");
        System.out.println("장영빈2");
        System.out.println("장영빈3");
        System.out.println("장영빈4");
        System.out.println("장영빈5");

        System.out.println("10" + "10"); // 문자열의 형태로 인식
        System.out.println("11" + "11"); // + 연산자(operator)
        System.out.println("12" + "12");

        System.out.println(20 + 20); // 정수라는 숫자의 형태로 인식
        System.out.println(21 + 21);
        System.out.println(22 + 22);
    }
}