public class Plus {
    public static void main(String[] args) {
        // method (메소드) call (호출)
        System.out.println(10 + " " + "10");
        System.out.println("20" + 20); // 문자열이 덧붙여진걸로 받아들임
        System.out.println(); // 줄 공백
        System.out.println(5.123);
        System.out.println(0.1 + 0.1);

        System.out.print(1);
        System.out.print(2);
        System.out.print(3);
    }
}